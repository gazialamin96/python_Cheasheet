import random
import math

types_of_soft_drinks = ['Soda', 'Coffee', 'Water', 'Tea']
print(random.choice(types_of_soft_drinks))  # it will print randomly list values
print(random.randint(1, 10))  # it will print random integer values

square_root = math.sqrt(64)
print(int(square_root))  # it will print square root of 64
